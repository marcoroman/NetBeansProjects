    //Method: penalty
    //Deducts 10 points from the score for an incorrect guess and increments the counter
    //indicating how many body parts have been drawn. It also checks whether the user has
    //Completed the hanged man or has lost too many points
    public void penalty(){
        points -= 10;
        ++anatomy;
        
        if(points <= 0 || anatomy >= 6){
            //HighScoresScreen hs = new HighScoresScreen();
            //hs.setVisible(true);
            ColorGame colorGame = new ColorGame();
            colorGame.setVisible(true);
            colorGame.addMouseListener(colorGame);
            this.setVisible(false);
        }       
        
        score.setText(Integer.toString(points));
        repaint();
    }
    
    //Method: paint
    //Draws the scaffold in the form and checks which body part should
    //be drawn to the screen
    @Override
    public void paint(Graphics g){
        
        super.paint(g);
        Graphics2D g2 = (Graphics2D) g;
        Line2D lin = new Line2D.Float(260, 65, 380, 65);
        Line2D lin2 = new Line2D.Float(260, 65, 260, 230);
        Line2D lin3 = new Line2D.Float(380, 65, 380, 90);
        Line2D lin4 = new Line2D.Float(190, 230, 450, 230);
        g2.draw(lin);
        g2.draw(lin2);
        g2.draw(lin3);
        g2.draw(lin4);
        
        Line2D torso = new Line2D.Float(380, 130, 380, 185);
        Line2D rightArm = new Line2D.Float(380, 135, 400, 155);
        Line2D leftArm = new Line2D.Float(380, 135, 360, 155);
        Line2D rightLeg = new Line2D.Float(380, 185, 400, 205);
        Line2D leftLeg = new Line2D.Float(380, 185, 360, 205);
        
        //Draws parts of the hanged man based on the value of the anatomy variable
        
        switch(anatomy){
            case 1: g2.drawOval(360, 90, 40, 40);
                break;
            case 2:
                g2.drawOval(360, 90, 40, 40);
                g2.draw(torso);
                break;
            case 3:
                g2.drawOval(360, 90, 40, 40);
                g2.draw(torso);
                g2.draw(rightArm);
                break;
            case 4:
                g2.drawOval(360, 90, 40, 40);
                g2.draw(torso);
                g2.draw(rightArm);
                g2.draw(leftArm);
                break;
            case 5:
                g2.drawOval(360, 90, 40, 40);
                g2.draw(torso);
                g2.draw(rightArm);
                g2.draw(leftArm);
                g2.draw(rightLeg);
                break;
            case 6:
                g2.drawOval(360, 90, 40, 40);
                g2.draw(torso);
                g2.draw(rightArm);
                g2.draw(leftArm);
                g2.draw(rightLeg);
                g2.draw(leftLeg);
                break;
            default: break;                        
        }
    }
    
    //Method: containsLetter
    //Checks the user's selected letter against the unknown word to determine whether the
    //guess was correct. Also checks whether the user has guessed the complete word
    public boolean containsLetter(char letter){
        boolean contains = false;
        
        for(int i = 0; i < word.length(); ++i){
            if(letter == word.charAt(i)){
                contains = true;
                spaces.deleteCharAt(i * 2);
                spaces.insert(i * 2, letter);
                wordDisplay.setText(spaces.toString());
                repaint();
                if(complete())
                    winner();
            }                
        }
        return contains;
    }
    
    //Method: complete
    //Checks whether the entire unknown word has been guessed
    public static boolean complete(){
        for(int i = 0; i < spaces.length(); ++i){
            if(spaces.charAt(i) == '_')
                return false;
        }
        
        points = 100;
        return true;
    }
    
