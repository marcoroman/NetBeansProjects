    public boolean complete(){
        boolean c = true;
        
        for(int i = 0; i < input.length; ++i){
            for(int j = 0; j < input.length; ++j){
                if(input[i][j] == 0){
                    c = false;
                }
            }
        }
        
        return c;
    }
    
    public boolean correct(){
        boolean c = true;
        for(int i = 0; i < key.length; ++i){
            for(int j = 0; j < key.length; ++j){
                if(key[i][j] != input[i][j]){
                    c = false;
                    points -= 10;
                    score -= 10;
                }
            }
        }
        
        return c;
    }
    
