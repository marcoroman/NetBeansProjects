    @Override
    public void paint(Graphics g){
        super.paint(g);
        
        g.setColor(Color.BLACK);
        
        int y1, y2;
        
        if(errorDisplay == false){
            y1 = 48;
            y2 = 390;
        }else{
            y1 = 34;
            y2 = 376;
        }
        
        for(int i = 0, x = 127; i < 4; ++i, x += 114){
            g.drawLine(x, y1, x, y2);
        }
        for (int i = 0, y = y1; i < 4; ++i, y += 114) {
            g.drawLine(127, y, 469, y);
        }

    }
    
