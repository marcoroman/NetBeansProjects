    public void valid(JFormattedTextField t, int i, int j){
        errorLabel.setVisible(false);
        errorDisplay = false;
        repaint();
        input[i][j] = Integer.parseInt(t.getText());
    }
    
    public void invalid(JFormattedTextField t){
        errorLabel.setVisible(true);
        errorDisplay = true;
        repaint();
        t.setText("");
    }
    
