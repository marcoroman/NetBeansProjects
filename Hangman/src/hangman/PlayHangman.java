    //Draws the scaffold in the form and begins a thread that checks which body part should
    //be drawn to the screen
    @Override
    public void paint(Graphics g){
        
        super.paint(g);
        Graphics2D g2 = (Graphics2D) g;
        Line2D lin = new Line2D.Float(280, 100, 400, 100);
        Line2D lin2 = new Line2D.Float(280, 100, 280, 270);
        Line2D lin3 = new Line2D.Float(400, 100, 400, 125);
        Line2D lin4 = new Line2D.Float(210, 270, 470, 270);
        g2.draw(lin);
        g2.draw(lin2);
        g2.draw(lin3);
        g2.draw(lin4);
        
        Line2D torso = new Line2D.Float(400, 165, 400, 220);
        Line2D rightArm = new Line2D.Float(400, 170, 420, 190);
        Line2D leftArm = new Line2D.Float(400, 170, 380, 190);
        Line2D rightLeg = new Line2D.Float(400, 220, 420, 240);
        Line2D leftLeg = new Line2D.Float(400, 220, 380, 240);
        
        switch(anatomy){
            case 1: g2.drawOval(380, 125, 40, 40);
                System.out.println("Case 1");
                break;
            case 2:
                g2.drawOval(380, 125, 40, 40);
                g2.draw(torso);
                System.out.println("Case 2");
                break;
            case 3:
                g2.drawOval(380, 125, 40, 40);
                g2.draw(torso);
                g2.draw(rightArm);
                System.out.println("Case 3");
                break;
            case 4:
                g2.drawOval(380, 125, 40, 40);
                g2.draw(torso);
                g2.draw(rightArm);
                g2.draw(leftArm);
                System.out.println("Case 4");
                break;
            case 5:
                g2.drawOval(380, 125, 40, 40);
                g2.draw(torso);
                g2.draw(rightArm);
                g2.draw(leftArm);
                g2.draw(rightLeg);
                System.out.println("Case 5");
                break;
            case 6:
                g2.drawOval(380, 125, 40, 40);
                g2.draw(torso);
                g2.draw(rightArm);
                g2.draw(leftArm);
                g2.draw(rightLeg);
                g2.draw(leftLeg);
                System.out.println("Case 6");
                break;
            default: break;                        
        }
    }
    
    //Method: containsLetter
    //Checks the user's selected letter against the unknown word to determine whether the
    //guess was correct. Also checks whether the user has guessed the complete word
    public static boolean containsLetter(char letter){
        boolean contains = false;
        
        for(int i = 0; i < word.length(); ++i){
            if(letter == word.charAt(i)){
                contains = true;
                spaces.deleteCharAt(i * 2);
                spaces.insert(i * 2, letter);
                wordDisplay.setText(spaces.toString());
                if(complete())
                    winner();
            }                
        }
        return contains;
    }
    
    //Method: complete
    //Checks whether the entire unknown word has been guessed
    public static boolean complete(){
        for(int i = 0; i < spaces.length(); ++i){
            if(spaces.charAt(i) == '_')
                return false;
        }
        
        return true;
    }
    
